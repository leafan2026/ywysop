import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const contentRoot = path.join(root, "content");
const metaPath = path.join(contentRoot, "sop-docs.meta.json");
const linksPath = path.join(contentRoot, "sop-links.json");

const linkConfig = fs.existsSync(linksPath)
  ? JSON.parse(fs.readFileSync(linksPath, "utf8"))
  : { sheet: {}, view: {} };

function parseScalar(value) {
  const trimmed = String(value).trim();
  if (trimmed === "true") return true;
  if (trimmed === "false") return false;
  if (/^-?\d+(\.\d+)?$/.test(trimmed)) return Number(trimmed);
  if (
    (trimmed.startsWith('"') && trimmed.endsWith('"')) ||
    (trimmed.startsWith("'") && trimmed.endsWith("'"))
  ) {
    try {
      return JSON.parse(trimmed);
    } catch {
      return trimmed.slice(1, -1);
    }
  }
  return trimmed;
}

function readMarkdown(filePath) {
  const source = fs.readFileSync(filePath, "utf8").replace(/^\uFEFF/, "");
  if (!source.startsWith("---\n")) {
    return { data: {}, body: source.trim() };
  }

  const end = source.indexOf("\n---", 4);
  if (end === -1) {
    throw new Error(`Missing frontmatter close: ${filePath}`);
  }

  const raw = source.slice(4, end);
  const body = source.slice(end + "\n---".length).replace(/^\n/, "").trim();
  const data = {};

  for (const line of raw.split("\n")) {
    if (!line.trim() || line.trim().startsWith("#")) continue;
    const colon = line.indexOf(":");
    if (colon === -1) continue;
    const key = line.slice(0, colon).trim();
    const value = line.slice(colon + 1).trim();
    data[key] = parseScalar(value);
  }

  return { data, body };
}

function linkChip(type, text, href) {
  return `<a class="chip chip--${type}" href="${href}" target="_blank" rel="noopener">${text}</a>`;
}

function applySopLinks(html) {
  return String(html).replace(
    /<span class="chip chip--(sheet|view)">([\s\S]*?)<\/span>/g,
    (match, type, text) => {
      const href = linkConfig[type]?.[text.trim()];
      if (!href) return match;
      return linkChip(type, text, href);
    }
  );
}

const meta = JSON.parse(fs.readFileSync(metaPath, "utf8"));
const docs = meta.docs.map((doc) => {
  const sections = doc.sections.map((entry) => {
    const filePath = path.join(contentRoot, entry.file);
    const { data, body } = readMarkdown(filePath);
    return {
      id: data.id || entry.id,
      title: data.title || data.id || entry.id,
      sortOrder: data.sort || 0,
      status: data.status || "active",
      context: data.context || "",
      screenshotNeed: data.screenshotNeed || "",
      mcpVerifyStatus: data.mcpVerifyStatus || "",
      mcpVerifyNote: data.mcpVerifyNote || "",
      bodyHtml: applySopLinks(body),
      sourceFile: path.relative(root, filePath),
    };
  });

  sections.sort((a, b) => Number(a.sortOrder) - Number(b.sortOrder));

  return {
    ...doc,
    sections: sections.map((section) => {
      const { file, ...clean } = section;
      return clean;
    }),
  };
});

const snapshot = {
  schemaVersion: 2,
  updatedAt: new Date().toISOString(),
  renderNotes: [
    "sourceOfTruth: content/**/*.md",
    "doNotEditManually: this JSON is generated by npm run build:content",
    "renderer: run npm run render:html to update published HTML files",
  ],
  docs,
};

const outputPath = path.join(root, "data", "sop-docs.json");
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${JSON.stringify(snapshot, null, 2)}\n`);

console.log(`Built ${docs.length} docs into data/sop-docs.json`);
